/**Quy chuẩn đặt tên trong lập trình:
 * Trong lập trình quy chuẩn đầu tiên cần đưa ra là quy chuẩn cho việc đặt tên.
 * Có hàng tỉ thứ cần đặt tên trong lập trình, nói chơi vậy thôi chứ phân loại ra khoảng hơn chục thôi à,
 * ví dụ như tên Class, tên biến, tên phương thức, tên thuộc tính...
 * Có 3 chuẩn để đặt tên là underscore, camelCase và PascalCase.
 * - underscore: sử dụng dấu gạch chân giữa các từ, tất cả các từ đều viết thường, ví dụ: $this_is_my_variable.
 * - camelCase: giống như cách viết của nó, từ đầu tiên viết thường, các từ tiếp theo viết hoa chữ cái đầu, ví dụ $thisIsMyVariable.
 * - PascalCase: viết hoa tất cả các chữ cái đầu, ví dụ $ThisIsMyVariable.
 */