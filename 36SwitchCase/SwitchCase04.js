let day = new Date().getDay();

switch (day) {
    case 0:
        console.log('Chủ Nhật');
        break;
    case 1:
        console.log('Thứ Hai');
        break;
    case 2:
        console.log('Thứ Ba');
        break;
    case 3:
        console.log('Thứ Tư');
        break;
    case 4:
        console.log('Thứ Năm');
        break;
    case 5:
        console.log('Thứ Sáu');
        break;
    case 6:
        console.log('Thứ Bảy');
        break;
}